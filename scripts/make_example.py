from __future__ import annotations

from pathlib import Path

import openpyxl

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "data" / "input"


def build_workbook(info: dict, balance: dict, pl: dict, path: Path) -> None:
    """Собирает .xlsx из словарей {код: [тек, пред, позапр]}."""
    wb = openpyxl.Workbook()

    ws_info = wb.active
    ws_info.title = "Сведения об организации"
    ws_info.append(["Показатель", "Значение"])
    ws_info.append(["Полное наименование организации", info["name"]])
    ws_info.append(["ИНН", info["inn"]])
    ws_info.append(["Отчётный период", info["period"]])

    ws_bal = wb.create_sheet("Бухгалтерский баланс")
    ws_bal.append(["Наименование показателя", "Код", "На тек. год",
                   "На пред. год", "На позапрошлый год"])
    for name, code, vals in balance["rows"]:
        ws_bal.append([name, code, *vals])

    ws_pl = wb.create_sheet("Отчет о финансовых результатах")
    ws_pl.append(["Наименование показателя", "Код", "За тек. год", "За пред. год"])
    for name, code, vals in pl["rows"]:
        ws_pl.append([name, code, *vals])

    path.parent.mkdir(parents=True, exist_ok=True)
    wb.save(path)
    print(f"Создан: {path.relative_to(ROOT)}")


